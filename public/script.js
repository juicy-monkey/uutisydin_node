const baseImageURL = "https://juicy-monkey.github.io/uutisydin_node";
// const baseImageURL = "file:///Users/jusa/workspace/Juicy_Monkey/projects/uutisydin/uutisydin_node/public";

function displayImages(imageData) {
  const container = document.getElementById("image-list");
  
  for (const keyword in imageData) {
    // Category title
    const categoryTitle = document.createElement("h2");
    categoryTitle.textContent = keyword;
    container.appendChild(categoryTitle);
    
    const list = document.createElement("div");
    list.className = "list";
    
    imageData[keyword].forEach(image => {
      const source = `${baseImageURL}/images/${keyword}/${image.filename}`;
      console.log(source)

      const imgDiv = document.createElement("div");
      imgDiv.className = "img-div";

      const img = document.createElement("img");
      img.src = source;
      img.alt = `${keyword}/${image.filename}`;
      img.loading = "lazy";

      const link = document.createElement("a");
      link.href = image.source;
      link.textContent = image.source;
      link.target = "_blank";
      link.rel = "noopener noreferrer";

      const license = document.createElement("p");
      license.textContent = `License: ${image.license}`;

      imgDiv.appendChild(img);
      imgDiv.appendChild(link);
      imgDiv.appendChild(license);
      list.appendChild(imgDiv);
    });

    container.appendChild(list);
  }
}

// Run when DOM is loaded
document.addEventListener("DOMContentLoaded", () => {
  displayImages(images);
});

